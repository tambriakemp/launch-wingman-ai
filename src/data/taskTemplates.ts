export const getTasksForFunnelType = (funnelType: string): TaskTemplate[] => {
  return TASK_TEMPLATES.filter(
    t => t.funnelTypes.includes('all') || t.funnelTypes.includes(funnelType as any)
  );
};

// Helper to get universal tasks (planning phase)
export const getUniversalTasks = (): TaskTemplate[] => {
  return TASK_TEMPLATES.filter(t => t.funnelTypes.includes('all'));
};
